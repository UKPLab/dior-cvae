    def _recurrent_latent(self, post_latent, prior_latent_representaion, weight_ih, weight_hh):
        h = self.tanh_activate(weight_hh(prior_latent_representaion) + weight_ih(post_latent))
        return h

    def get_prior(self, batch_size, device, condition=None, condition_mask=None):
        if condition is not None:
            condition_output = self(condition, attention_mask=condition_mask, compute_kl=False)
            condition_hidden_states = condition_output.hidden_states
        else:
            condition_hidden_states = tuple([None] * self.num_layers)
        if not self.diffusion_prior:
            prior_dist = Normal.get_standard(batch_size, self.latent_size, device)
            prior_latent_representaion = torch.zeros(batch_size, self.latent_size).to(device)
            prior_latent = torch.zeros_like(prior_latent_representaion)
            all_prior_latent = ()

            for idx in range(self.begin_layer, self.end_layer + 1):
                cond = condition_hidden_states[idx - self.begin_layer]
                prior_network = self.prior_network[idx - self.begin_layer]
                weight_ih = self.reccurnt_cell_weight_ih[idx - self.begin_layer]
                weight_hh = self.reccurnt_cell_weight_hh[idx - self.begin_layer]
                prior_latent_representaion = self._recurrent_latent(prior_latent, prior_latent_representaion, weight_ih, weight_hh)
                if cond is not None:
                    prior_representaion = torch.cat((prior_latent_representaion, cond), dim=-1)
                else:
                    prior_representaion = prior_latent_representaion
                prior_mu, prior_sigma = torch.chunk(prior_network(prior_representaion), 2, dim=-1)
                prior_dist = Normal(prior_mu, prior_sigma)
                prior_latent, _ = prior_dist.sample()
                all_prior_latent = all_prior_latent + (prior_latent, )
        else:
            cat_condition_state = self.condition_state_transform(
                torch.flatten(torch.stack(condition_hidden_states).permute(1, 0, 2), start_dim=1, end_dim=-1))
            model_kwargs = {}
            model_kwargs["y"] = cat_condition_state
            model_kwargs["w"] = self.w
            sample_fn = (
                self.diffusion.p_sample_loop if not self.use_ddim else self.diffusion.ddim_sample_loop
            )
            sample = sample_fn(
                self.diff_predict_net,
                (cat_condition_state.shape[0], cat_condition_state.shape[1]),
                clip_denoised=self.clip_denoised,
                model_kwargs=model_kwargs,
            )
            # sample = torch.randn_like(cat_condition_state).to(cat_condition_state.device)
            all_prior_latent_list = list(
                torch.chunk(sample.reshape(-1, len(self.layers), self.latent_size).permute(1, 0, 2),
                            len(self.layers)))
            all_prior_latent = ()
            for i in range(len(all_prior_latent_list)):
                all_prior_latent = all_prior_latent + (all_prior_latent_list[i].squeeze(0), )

        return all_prior_latent

    def forward(
        self,
        input_ids=None,
        attention_mask=None,
        head_mask=None,
        inputs_embeds=None,
        output_attentions=None,
        output_hidden_states=None,
        return_dict=None,
        condition_hidden_states=None,
        compute_kl=True,
        past_key_values=None,
        use_cache=None,
        latent=None
    ):
        r"""
        Args:
            input_ids (:obj:`torch.LongTensor` of shape :obj:`(batch_size, sequence_length)`):
                Indices of input sequence tokens in the vocabulary. Padding will be ignored by default should you
                provide it.

                Indices can be obtained using :class:`~transformers.BartTokenizer`. See
                :meth:`transformers.PreTrainedTokenizer.encode` and :meth:`transformers.PreTrainedTokenizer.__call__`
                for details.

                `What are input IDs? <../glossary.html#input-ids>`__
            attention_mask (:obj:`torch.Tensor` of shape :obj:`(batch_size, sequence_length)`, `optional`):
                Mask to avoid performing attention on padding token indices. Mask values selected in ``[0, 1]``:

                - 1 for tokens that are **not masked**,
                - 0 for tokens that are **masked**.

                `What are attention masks? <../glossary.html#attention-mask>`__
            head_mask (:obj:`torch.Tensor` of shape :obj:`(encoder_layers, encoder_attention_heads)`, `optional`):
                Mask to nullify selected heads of the attention modules. Mask values selected in ``[0, 1]``:

                - 1 indicates the head is **not masked**,
                - 0 indicates the head is **masked**.

            inputs_embeds (:obj:`torch.FloatTensor` of shape :obj:`(batch_size, sequence_length, hidden_size)`, `optional`):
                Optionally, instead of passing :obj:`input_ids` you can choose to directly pass an embedded
                representation. This is useful if you want more control over how to convert :obj:`input_ids` indices
                into associated vectors than the model's internal embedding lookup matrix.
            output_attentions (:obj:`bool`, `optional`):
                Whether or not to return the attentions tensors of all attention layers. See ``attentions`` under
                returned tensors for more detail.
            output_hidden_states (:obj:`bool`, `optional`):
                Whether or not to return the hidden states of all layers. See ``hidden_states`` under returned tensors
                for more detail.
            return_dict (:obj:`bool`, `optional`):
                Whether or not to return a :class:`~transformers.file_utils.ModelOutput` instead of a plain tuple.
        """
        output_attentions = output_attentions if output_attentions is not None else self.config.output_attentions
        output_hidden_states = (
            output_hidden_states if output_hidden_states is not None else self.config.output_hidden_states
        )
        return_dict = return_dict if return_dict is not None else self.config.use_return_dict

        # retrieve input_ids and inputs_embeds
        if input_ids is not None and inputs_embeds is not None:
            raise ValueError("You cannot specify both input_ids and inputs_embeds at the same time")
        elif input_ids is not None:
            input_shape = input_ids.size()
            input_ids = input_ids.view(-1, input_shape[-1])
        elif inputs_embeds is not None:
            input_shape = inputs_embeds.size()[:-1]
        else:
            raise ValueError("You have to specify either input_ids or inputs_embeds")

        if condition_hidden_states is None:
            condition_hidden_states = tuple([None] * len(self.layers))
        else:
            assert self.is_cvae

        if past_key_values is None:
            past_key_values = tuple([None] * len(self.layers))

        if inputs_embeds is None:
            inputs_embeds = self.embed_tokens(input_ids) * self.embed_scale

        embed_pos = self.embed_positions(input_shape)

        hidden_states = inputs_embeds + embed_pos
        hidden_states = self.layernorm_embedding(hidden_states)
        hidden_states = nn.functional.dropout(hidden_states, p=self.dropout, training=self.training)

        # expand attention_mask
        if attention_mask is not None:
            # [bsz, seq_len] -> [bsz, 1, tgt_seq_len, src_seq_len]
            attention_mask = _expand_mask(attention_mask, inputs_embeds.dtype)

        device = input_ids.device if input_ids is not None else inputs_embeds.device
        batch_size = inputs_embeds.shape[0]
        encoder_states = () if output_hidden_states else None
        all_attentions = () if output_attentions else None
        prior_dist = Normal.get_standard(batch_size, self.latent_size, device)
        prior_latent_representaion = torch.zeros(batch_size, self.latent_size).to(device)
        post_latent = torch.zeros_like(prior_latent_representaion)
        all_post_latent = ()
        all_kl_loss = ()
        presents = () if use_cache else None
        all_self_attentions = () if output_attentions else None
        all_hidden_states = ()
        all_log_prior = ()
        all_log_post = ()
        all_post_mu = ()
        all_post_sigma = ()
        loops = zip(self.layers, past_key_values, self.pooling, condition_hidden_states, self.recog_network,
                    self.prior_network, self.reccurnt_cell_weight_ih, self.reccurnt_cell_weight_hh)

        # check if head_mask has a correct number of layers specified if desired
        if head_mask is not None:
            assert head_mask.size()[0] == (
                len(self.layers)
            ), f"The head_mask should be specified for {len(self.layers)} layers, but it is for {head_mask.size()[0]}."
        for idx, (encoder_layer, layer_past, pooling_layer, condition, recog_network, prior_network, weight_ih, weight_hh) in enumerate(loops):
            if self.model_parallel:
                torch.cuda.set_device(hidden_states.device)
                # Ensure layer_past is on same device as hidden_states (might not be correct)
                if layer_past is not None:
                    layer_past = tuple(past_state.to(hidden_states.device) for past_state in layer_past)
                # Ensure that attention_mask is always on the same device as hidden_states
                if attention_mask is not None:
                    attention_mask = attention_mask.to(hidden_states.device)
                if isinstance(head_mask, torch.Tensor):
                    head_mask = head_mask.to(hidden_states.device)

            if output_hidden_states:
                encoder_states = encoder_states + (hidden_states,)
            # add LayerDrop (see https://arxiv.org/abs/1909.11556 for description)
            dropout_probability = random.uniform(0, 1)
            if self.training and (dropout_probability < self.layerdrop):  # skip the layer
                layer_outputs = (None, None)
            else:
                if self.gradient_checkpointing and self.training:

                    def create_custom_forward(module):
                        def custom_forward(*inputs):
                            return module(*inputs, output_attentions)

                        return custom_forward

                    layer_outputs = torch.utils.checkpoint.checkpoint(
                        create_custom_forward(encoder_layer),
                        hidden_states,
                        attention_mask,
                        (head_mask[idx] if head_mask is not None else None),
                    )
                else:
                    layer_outputs = encoder_layer(
                        hidden_states,
                        attention_mask,
                        layer_head_mask=(head_mask[idx] if head_mask is not None else None),
                        output_attentions=output_attentions,
                    )

                hidden_states = layer_outputs[0]
                representation, _ = pooling_layer(hidden_states)
                all_hidden_states = all_hidden_states + (representation,)
                if compute_kl and self.begin_layer <= idx <= self.end_layer:
                    # update prior latent
                    prior_latent_representaion = self._recurrent_latent(post_latent, prior_latent_representaion,
                                                                        weight_ih, weight_hh)
                    if condition is not None:
                        prior_representaion = torch.cat((prior_latent_representaion, condition), dim=-1)
                    else:
                        prior_representaion = prior_latent_representaion
                    prior_mu, prior_sigma = torch.chunk(prior_network(prior_representaion), 2, dim=-1)
                    prior_dist = Normal(prior_mu, prior_sigma)

                    post_latent_representation = recog_network(
                        torch.cat((representation, prior_latent_representaion), dim=-1))
                    post_mu, post_sigma = torch.chunk(post_latent_representation, 2, dim=-1)
                    post_dist = Normal(post_mu, post_sigma)
                    post_latent, _ = post_dist.sample()
                    all_post_latent = all_post_latent + (post_latent,)
                    kl_loss = post_dist.kl(prior_dist)
                    kl_threshold = torch.Tensor([self.kl_threshold]).type_as(kl_loss)
                    kl_loss = torch.max(kl_loss, kl_threshold)

                    log_prior_z = prior_dist.log_p(post_latent)
                    log_post_z = post_dist.log_p(post_latent)
                    if self.model_parallel:
                        kl_loss = kl_loss.to(self.first_device)
                        log_prior_z = log_prior_z.to(self.first_device)
                        log_post_z = log_post_z.to(self.first_device)
                    all_kl_loss = all_kl_loss + (kl_loss,)
                    all_log_prior = all_log_prior + (log_prior_z,)
                    all_log_post = all_log_post + (log_post_z,)
                    all_post_mu = all_post_mu + (post_mu,)
                    all_post_sigma = all_post_sigma + (post_sigma,)
            if use_cache is True:
                presents = presents + (layer_outputs[1],)

            if output_attentions:
                all_attentions = all_attentions + (layer_outputs[1],)
                # all_cross_attentions = all_cross_attentions + (layer_outputs[3 if use_cache else 2],)

            if self.model_parallel:
                for k, v in self.device_map.items():
                    if idx == v[-1] and "cuda:" + str(k) != self.last_device:
                        hidden_states = hidden_states.to("cuda:" + str(k + 1))

        if output_hidden_states:
            encoder_states = encoder_states + (hidden_states,)

        if compute_kl:
            if not self.diffusion_prior:
                kl_loss = torch.stack(all_kl_loss).mean(0)
                log_prior = torch.stack(all_log_prior).mean(0)
                log_post = torch.stack(all_log_post).mean(0)

                mu = torch.cat(all_post_mu, dim=-1)
                sigma = torch.cat(all_post_sigma, dim=-1)
            else:
                cat_post_latent = torch.flatten(torch.stack(all_post_latent).permute(1, 0, 2), start_dim=1, end_dim=-1)
                cat_condition_state = self.condition_state_transform(
                    torch.flatten(torch.stack(condition_hidden_states).permute(1, 0, 2), start_dim=1, end_dim=-1))
                vae_neg_entropy = 0.0
                for log_q_conv in all_log_post:
                    vae_neg_entropy += torch.sum(log_q_conv, dim=-1)

                micro_cond = {}
                micro_cond['y'] = cat_condition_state
                t, weights = self.schedule_sampler.sample(hidden_states.shape[0], hidden_states.device)
                compute_losses = functools.partial(
                    self.diffusion.training_losses,
                    self.diff_predict_net,
                    cat_post_latent,
                    t,
                    True,
                    model_kwargs=micro_cond,
                )
                losses = compute_losses()
                loss = losses['loss']
                p_objective = torch.sum(weights * loss, dim=-1)
                cross_entropy_per_var = weights.unsqueeze(-1) * loss.unsqueeze(-1)
                cross_entropy_per_var = cross_entropy_per_var + self.diffusion.cross_entropy_const(
                    torch.zeros_like(t, dtype=t.dtype, device=t.device), cat_post_latent)
                cross_entropy = torch.sum(cross_entropy_per_var, dim=-1)
                all_neg_log_p = list(torch.chunk(
                    cross_entropy_per_var.reshape(-1, len(self.layers), self.latent_size).permute(1, 0, 2),
                    len(self.layers)))
                for i in range(len(all_neg_log_p)):
                    all_neg_log_p[i] = all_neg_log_p[i].squeeze(0)
                kl_all_list, kl_vals_per_group, kl_diag_list = utils.kl_per_group_vada(all_log_post, all_neg_log_p)
                kl_coeff = 1.0
                alpha_i = torch.ones([len(self.layers)], device=cat_condition_state.device)
                balanced_kl, kl_coeffs, kl_vals = utils.kl_balancer(kl_all_list, kl_coeff, kl_balance=False,
                                                                    alpha_i=alpha_i)
                kl_loss = torch.sum(balanced_kl)
                log_prior = torch.stack(all_log_prior).mean(0)
                log_post = torch.stack(all_log_post).mean(0)

                mu = torch.cat(all_post_mu, dim=-1)
                sigma = torch.cat(all_post_sigma, dim=-1)
        else:
            kl_loss = None
            log_prior = None
            log_post = None
            mu, sigma = None, None

        if self.model_parallel:
            torch.cuda.set_device(self.first_device)
            hidden_states = hidden_states.to(self.first_device)

        if not return_dict:
            return tuple(v for v in [hidden_states, encoder_states, all_attentions] if v is not None)
        return EncoderOutput(
            last_hidden_state=hidden_states,
            past_key_values=presents,
            hidden_states=all_hidden_states,
            attentions=all_attentions,
            latent=all_post_latent,
            dist_parameter=(mu, sigma),
            kl_loss=kl_loss,
            log_prior=log_prior,
            log_post=log_post,
        )
